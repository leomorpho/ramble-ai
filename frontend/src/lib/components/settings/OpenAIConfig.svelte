<script>
  import { Button } from "$lib/components/ui/button";
  import { GetOpenAIApiKey, SaveOpenAIApiKey, DeleteOpenAIApiKey } from "$lib/wailsjs/go/main/App";
  import { onMount } from "svelte";

  let openaiApiKey = $state("");
  let saved = $state(false);
  let loading = $state(false);
  let error = $state("");
  let showApiKey = $state(false);

  onMount(() => {
    loadApiKey();
  });

  async function loadApiKey() {
    try {
      loading = true;
      error = "";
      const savedKey = await GetOpenAIApiKey();
      if (savedKey) {
        openaiApiKey = savedKey;
      }
    } catch (err) {
      console.error("Failed to load API key:", err);
      error = "Failed to load API key";
    } finally {
      loading = false;
    }
  }

  async function saveApiKey() {
    try {
      loading = true;
      error = "";
      await SaveOpenAIApiKey(openaiApiKey);
      saved = true;
      
      setTimeout(() => {
        saved = false;
      }, 2000);
    } catch (err) {
      console.error("Failed to save API key:", err);
      error = "Failed to save API key";
    } finally {
      loading = false;
    }
  }

  async function clearApiKey() {
    try {
      loading = true;
      error = "";
      openaiApiKey = "";
      await DeleteOpenAIApiKey();
      saved = true;
      
      setTimeout(() => {
        saved = false;
      }, 2000);
    } catch (err) {
      console.error("Failed to clear API key:", err);
      error = "Failed to clear API key";
    } finally {
      loading = false;
    }
  }

  function toggleApiKeyVisibility() {
    showApiKey = !showApiKey;
  }

</script>

<div class="bg-card border rounded-lg p-6 space-y-6">
  <div class="space-y-2">
    <h2 class="text-xl font-semibold">OpenAI Configuration</h2>
    <p class="text-muted-foreground text-sm">
      Configure your OpenAI API key to enable Whisper transcription features.
    </p>
  </div>

  {#if !openaiApiKey.trim()}
    <div class="bg-secondary/50 border border-border rounded-lg p-4 space-y-3">
      <h3 class="text-sm font-medium text-foreground">ðŸš€ First time setup? Get your OpenAI API key:</h3>
      <ol class="text-sm text-muted-foreground space-y-2 ml-4">
        <li class="flex items-start gap-2">
          <span class="bg-primary/10 text-primary text-xs px-1.5 py-0.5 rounded font-mono">1</span>
          <div>
            Go to <a href="https://platform.openai.com/api-keys" target="_blank" class="text-primary hover:underline font-medium">platform.openai.com/api-keys</a>
          </div>
        </li>
        <li class="flex items-start gap-2">
          <span class="bg-primary/10 text-primary text-xs px-1.5 py-0.5 rounded font-mono">2</span>
          <div>Sign in or create an OpenAI account</div>
        </li>
        <li class="flex items-start gap-2">
          <span class="bg-primary/10 text-primary text-xs px-1.5 py-0.5 rounded font-mono">3</span>
          <div>Click "Create new secret key" and give it a name</div>
        </li>
        <li class="flex items-start gap-2">
          <span class="bg-primary/10 text-primary text-xs px-1.5 py-0.5 rounded font-mono">4</span>
          <div>Copy the key (starts with "sk-") and paste it below</div>
        </li>
        <li class="flex items-start gap-2">
          <span class="bg-primary/10 text-primary text-xs px-1.5 py-0.5 rounded font-mono">5</span>
          <div>ðŸ’¡ <strong>Pro tip:</strong> Add billing info to your OpenAI account to avoid rate limits</div>
        </li>
      </ol>
      <div class="text-xs text-muted-foreground mt-3">
        <strong>What you'll get:</strong> Automatic transcription, speech-to-text conversion, and subtitle generation for your videos.
        <br>
        <strong>Cost:</strong> ~$0.006 per minute of audio. <a href="https://openai.com/pricing" target="_blank" class="text-primary hover:underline">See pricing details</a>
      </div>
    </div>
  {/if}

  <div class="space-y-4">
    <div class="space-y-2">
      <label for="api-key" class="text-sm font-medium">
        OpenAI API Key
      </label>
      <div class="relative">
        <input
          id="api-key"
          type={showApiKey ? "text" : "password"}
          bind:value={openaiApiKey}
          placeholder="sk-..."
          disabled={loading}
          class="w-full px-3 py-2 pr-10 border border-input rounded-md bg-background text-foreground focus:outline-none focus:ring-2 focus:ring-ring disabled:opacity-50"
        />
        <button
          type="button"
          onclick={toggleApiKeyVisibility}
          class="absolute right-2 top-1/2 -translate-y-1/2 p-1 text-muted-foreground hover:text-foreground transition-colors"
          disabled={loading}
        >
          {#if showApiKey}
            <svg class="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.543-7a9.97 9.97 0 011.563-3.029m5.858.908a3 3 0 114.243 4.243M9.878 9.878l4.242 4.242M9.878 9.878L3 3m6.878 6.878L21 21" />
            </svg>
          {:else}
            <svg class="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M15 12a3 3 0 11-6 0 3 3 0 016 0z" />
              <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z" />
            </svg>
          {/if}
        </button>
      </div>
      <p class="text-xs text-muted-foreground">
        Your API key is stored securely in the local database and never sent to external servers.
      </p>
    </div>

    <div class="flex gap-2">
      <Button 
        onclick={saveApiKey}
        disabled={loading || !openaiApiKey.trim()}
        class="flex-1"
      >
        {loading ? "Saving..." : saved ? "Saved!" : "Save API Key"}
      </Button>
      
      
      {#if openaiApiKey}
        <Button 
          variant="outline" 
          onclick={clearApiKey}
          disabled={loading}
          class="flex-1"
        >
          {loading ? "Clearing..." : "Clear"}
        </Button>
      {/if}
    </div>

    {#if error}
      <div class="bg-destructive/10 text-destructive border border-destructive/20 rounded-md p-3 text-sm">
        {error}
      </div>
    {/if}


    {#if saved && !error}
      <div class="bg-green-50 border border-green-200 text-green-800 rounded-md p-3 text-sm">
        API key saved successfully!
      </div>
    {/if}
  </div>
</div>